const (
	componentName          = "pilot"
	serviceAccountName     = "istio-pilot-service-account"
	clusterRoleName        = "istio-pilot-cluster-role"
	clusterRoleBindingName = "istio-pilot-cluster-role-binding"
	deploymentName         = "istio-pilot"
	serviceName            = "istio-pilot"
	hpaName                = "istio-pilot-autoscaler"
	gatewayName            = "istio-autogenerated-k8s-ingress"
)

var pilotLabels = map[string]string{
	"app": "istio-pilot",
}

var labelSelector = map[string]string{
	"istio": "pilot",
}

type Reconciler struct {
	resources.Reconciler
	dynamic dynamic.Interface
}

func New(client client.Client, dc dynamic.Interface, config *istiov1beta1.Config) *Reconciler {
	return &Reconciler{
		Reconciler: resources.Reconciler{
			Client: client,
			Config: config,
		},
		dynamic: dc,
	}
}

func (r *Reconciler) Reconcile(log logr.Logger) error {
	log = log.WithValues("component", componentName)

	log.Info("Reconciling")

	for _, res := range []resources.Resource{
		r.serviceAccount,
		r.clusterRole,
		r.clusterRoleBinding,
		r.deployment,
		r.service,
		r.horizontalPodAutoscaler,
	} {
		o := res()
		err := k8sutil.Reconcile(log, r.Client, o)
		if err != nil {
			return emperror.WrapWith(err, "failed to reconcile resource", "resource", o.GetObjectKind().GroupVersionKind())
		}
	}
	drs := []resources.DynamicResourceWithDesiredState{
		{DynamicResource: r.gateway, DesiredState: k8sutil.CREATED},
	}
	for _, dr := range drs {
		o := dr.DynamicResource()
		err := o.Reconcile(log, r.dynamic, dr.DesiredState)
		if err != nil {
			return emperror.WrapWith(err, "failed to reconcile dynamic resource", "resource", o.Gvr)
		}
	}

	log.Info("Reconciled")

	return nil
}
